def test_beads_markers_assertion_fails_when_marker_missing(tmp_path: Path, monkeypatch) -> None:
    target = tmp_path / "AGENTS.md"
    # Establish the baseline first by running the check on the
    # pristine file (which has INTEGRATION + CODEX SETUP).
    target.write_text(
        "<!-- BEGIN BEADS INTEGRATION v:1 profile:minimal hash:abc -->\n"
        "body\n"
        "<!-- END BEADS INTEGRATION -->\n"
        "\n"
        "<!-- BEGIN BEADS CODEX SETUP: generated by bd setup codex -->\n"
        "body\n"
        "<!-- END BEADS CODEX SETUP -->\n",
        encoding="utf-8",
    )
    monkeypatch.chdir(tmp_path)
    # Confine the baseline carrier to ``tmp_path`` so this test never
    # touches the active repository's baseline file (the
    # ``example-008.1`` fix correctly walks up to the project's ``.git/``).
    monkeypatch.setattr(markers, "_find_repo_cwd", lambda _p: tmp_path)
    # First call captures the baseline.
    markers.assert_beads_markers_untouched(target)

    # Now delete the CODEX SETUP block; the check must fail with the
    # new line-anchored form.
    target.write_text(
        "<!-- BEGIN BEADS INTEGRATION v:1 profile:minimal hash:abc -->\n"
        "body\n"
        "<!-- END BEADS INTEGRATION -->\n",
        encoding="utf-8",
    )
    ok, reason = markers.assert_beads_markers_untouched(target)
    assert ok is False
    assert "BEADS CODEX SETUP" in reason
    assert "begin=" in reason and "end=" in reason


