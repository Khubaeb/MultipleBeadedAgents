def test_mba_rules_block_requires_launch_receipt_in_same_launch_step() -> None:
    """Downstream smoke test exposed that the prior wording allowed
    the Orchestrator to write the launch receipt **after** the
    worker produced `result.md` or a Bead comment. The installed MBA
    RULES block must require the receipt to be written **in the same
    launch step** that captures the worker's PID or session id, and
    **before** any wait / tail / read / acceptance of worker output.

    The test fails if the wording is weakened back to a bare
    "before accepting outputs" rule with no temporal ordering — that
    is exactly the wording the Downstream smoke test shipped.
    """

    import re

    text = markers.MBA_RULES_BLOCK
    # ``same launch step`` wording pins the temporal ordering.
    assert "same launch step" in text, (
        "MBA RULES block must require the launch receipt to be written "
        "in the same launch step that captures the worker's PID or "
        "session id (Downstream smoke test acceptance criterion)."
    )
    # ``before`` the wait/tail/read/acceptance — explicit list of what
    # is forbidden before the receipt is written.
    assert re.search(
        r"before[\s\S]{0,80}wait[\s\S]{0,80}tail[\s\S]{0,80}read[\s\S]{0,80}accept",
        text,
    ), (
        "MBA RULES block must list the wait/tail/read/accept steps "
        "that the Orchestrator must not perform before writing the "
        "launch receipt."
    )
    # Writing the receipt **after** worker output is a refusal-grade
    # error — the receipt no longer proves the session existed.
    assert "after" in text.lower() and "refusal-grade" in text.lower(), (
        "MBA RULES block must explicitly forbid writing the launch "
        "receipt after any worker output exists."
    )


