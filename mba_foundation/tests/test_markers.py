def test_find_repo_cwd_does_not_latch_onto_ancestor_pollution(
    tmp_path: Path,
) -> None:
    """``example-008.1`` regression: the upward walk must NOT return an
    ancestor that owns a stray ``.mba-work/``.

    The previous ``_find_repo_cwd`` returned the first ancestor with
    ``.mba-work/``, ``.beads/`` or ``.git/``. A stray ``.mba-work/``
    outside the active project (the ``C:\\.mba-work`` pollution from
    the ``example-007.1`` boundary incident) therefore captured the
    baseline and caused a write outside the active repository. The
    fix reorders the boundary checks: ``.git/`` first, ``.beads/``
    second, otherwise return the file's parent.

    The test runs under the prescribed repository-local
    ``--basetemp`` so ``tmp_path`` lives under the active
    repository's ``.git/`` boundary. With the fix the walk stops at
    that boundary (the project root) and never reaches the
    ``tmp_path`` ancestor that owns the pollution. Pre-fix code
    would have visited ``ancestor_dir`` and latched onto its
    ``.mba-work/`` pollution.
    """

    ancestor = tmp_path / "ancestor_dir"
    active = ancestor / "active_repo"
    ancestor.mkdir()
    active.mkdir()
    # The sandbox has NO ``.git/`` and NO ``.beads/`` so without the
    # fix the walk would have to keep climbing past ``ancestor_dir``.
    assert not (active / ".git").exists()
    assert not (active / ".beads").exists()

    pollution = ancestor / ".mba-work" / ".marker-baseline.json"
    pollution_payload = (
        '{"ancestor/AGENTS.md": ["BEADS INTEGRATION"]}\n'
    )
    pollution.parent.mkdir(parents=True, exist_ok=True)
    pollution.write_text(pollution_payload, encoding="utf-8")

    target = active / "AGENTS.md"
    target.write_text(
        "<!-- BEGIN BEADS INTEGRATION v:1 profile:minimal hash:abc -->\n"
        "body\n"
        "<!-- END BEADS INTEGRATION -->\n",
        encoding="utf-8",
    )

    result = markers._find_repo_cwd(target)
    # The walk must NOT have returned the ancestor (which owns the
    # pollution). With the fix the walk climbs past ``ancestor_dir``
    # until it reaches the active repository's ``.git/`` and returns
    # that. Without the fix the walk would have returned
    # ``ancestor_dir`` and the test below would fail.
    assert result != ancestor, (
        f"_find_repo_cwd returned ancestor {ancestor}; "
        "the walk latched onto pollution. The example-008.1 fix should "
        "have stopped the walk at the active project's .git/."
    )


def test_assert_beads_markers_untouched_preserves_ancestor_pollution(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """``example-008.1`` integration: a stray ``.mba-work/`` outside the
    confined baseline carrier is NOT touched by
    ``assert_beads_markers_untouched``.

    Pins ``_find_repo_cwd`` to ``active`` so the baseline carrier is
    local to the test sandbox; this isolates the integration check
    from the project-root walk-up exercised separately by
    ``test_find_repo_cwd_does_not_latch_onto_ancestor_pollution``.
    """

    ancestor = tmp_path / "ancestor_dir"
    active = ancestor / "active_repo"
    ancestor.mkdir()
    active.mkdir()

    pollution = ancestor / ".mba-work" / ".marker-baseline.json"
    pollution_payload = (
        '{"ancestor/AGENTS.md": ["BEADS INTEGRATION"]}\n'
    )
    pollution.parent.mkdir(parents=True, exist_ok=True)
    pollution.write_text(pollution_payload, encoding="utf-8")

    target = active / "AGENTS.md"
    target.write_text(
        "<!-- BEGIN BEADS INTEGRATION v:1 profile:minimal hash:abc -->\n"
        "body\n"
        "<!-- END BEADS INTEGRATION -->\n",
        encoding="utf-8",
    )

    # Confine the baseline carrier to ``active``.
    monkeypatch.setattr(markers, "_find_repo_cwd", lambda _p: active)

    ok, reason = markers.assert_beads_markers_untouched(target)
    assert ok is True, reason

    # Baseline lives inside the confined active repo, not the ancestor.
    fresh_baseline = active / ".mba-work" / ".marker-baseline.json"
    assert fresh_baseline.exists(), (
        "baseline was not captured inside the confined active repo"
    )

    # The ancestor pollution MUST be byte-for-byte preserved.
    assert pollution.read_text(encoding="utf-8") == pollution_payload


def test_find_repo_cwd_returns_active_repo_when_git_present(
    tmp_path: Path,
) -> None:
    """``example-008.1`` regression: when the active repo owns ``.git/``
    and an ancestor has a stray ``.mba-work/``, the walk stops at
    the active repo's ``.git/`` boundary.

    This is the typical real-world shape
    (``C:\\CODE\\MultipleBeadedAgents`` has ``.git/``); the fix
    reorders the boundary checks so the active repo's ``.git/``
    wins over any ancestor pollution.
    """

    ancestor = tmp_path / "ancestor_dir"
    active = ancestor / "active_repo"
    ancestor.mkdir()
    active.mkdir()
    (active / ".git").mkdir()

    pollution = ancestor / ".mba-work" / ".marker-baseline.json"
    pollution_payload = (
        '{"ancestor/AGENTS.md": ["BEADS INTEGRATION"]}\n'
    )
    pollution.parent.mkdir(parents=True, exist_ok=True)
    pollution.write_text(pollution_payload, encoding="utf-8")

    target = active / "AGENTS.md"
    target.write_text(
        "<!-- BEGIN BEADS INTEGRATION v:1 profile:minimal hash:abc -->\n"
        "body\n"
        "<!-- END BEADS INTEGRATION -->\n",
        encoding="utf-8",
    )

    result = markers._find_repo_cwd(target)
    assert result == active, (
        f"expected the walk to stop at the active repo {active}, "
        f"got {result}"
    )
    assert pollution.read_text(encoding="utf-8") == pollution_payload
